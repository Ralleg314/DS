    private void catalegValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_catalegValueChanged
        // TODO add your handling code here:
        int pos=cataleg.getSelectedIndex();
        if(pos!=-1 && last!=pos){
            OUTER:
            switch (place) {
                case 0:
                    serie=pos;
                    this.actualitzaLlista(cont.mostratTemporades(serie));
                    place=1;
                    break;
                case 1:
                    if(pos==0){
                        this.actualitzaLlista(cont.mostratCataleg());
                        place=0;
                    }else{
                        temporada=pos-1;
                        this.actualitzaLlista(cont.mostratEpisodis(serie,temporada));
                        place=2;
                    }   break;
                case 2:
                    if(pos==0){
                        this.actualitzaLlista(cont.mostratTemporades(serie));
                        place=1;
                    }else{
                        episodi=pos-1;
                        place=3;
                        this.actualitzaLlista(opt);
                    }   break;
                case 3:
                    switch (pos) {
                        case 0:
                            this.actualitzaLlista(cont.mostratEpisodis(serie,temporada));
                            place=2;
                            break;
                        case 1:
                            if(cont.userIsLoged()){
                                cont.reproduirEpisodi(serie, temporada, episodi);
                            }
                            break;
                        case 2:
                            if(cont.userIsLoged() && cont.getEstat(serie, temporada, episodi).equals("VIST")){
                                new ValorarDialog(this,true).setVisible(true);
                            }
                            break;
                    }
            }
        }
        last=pos;
    }//GEN-LAST:event_catalegValueChanged

    public void actualitzaLlista(ArrayList<String> llista){
        DefaultListModel temp = new DefaultListModel();
        for(String s : llista){
           temp.addElement(s);
        }
        cataleg.setModel(temp);
    }
    
